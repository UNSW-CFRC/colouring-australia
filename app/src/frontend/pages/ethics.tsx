<p>The Colouring Cities Research Programme (CCRP) has been designed to test a new type of free public information tool, to encourage knowledge and data sharing about buildings and cities, for the public good. Colouring Adelaide is being designed as a safe, positive, constructive space for users of diverse ages, genders, cultural backgrounds, skills and abilities to enjoy and benefit from. Users need to be sure their contributions will be treated with respect and principles of lawfulness, fairness and transparency, purpose limitation, data minimisation, accuracy, storage limitation, integrity and confidentiality (security), and accountability, will be met. We in fact actively discourage users from giving us personal data wherever possible. The privacy of building occupiers is also an important issue for us and is prioritised in our data collection approach. It is carefully balanced against the increasingly urgent need to collect information on building stocks to aid emissions reduction and increase urban sustainability as a whole.</p>

